package proyecto;

public class PrendasSeleccionadas {

    String prendaSuperiores;
    String prendaInferior;
    String calzados;
    
    String marcaSuperiores;
    String marcaInferiores;
    String marcaCalzados;
    
    String colorSuperior;
    String colorInferior;
    String colorCalzado;
    
    String tallaSuperior;
    String tallaInferior;
    String tallaCalzado;
    
    double precioSuperior;
    double precioInferior;
    double precioCalzado;

    public PrendasSeleccionadas(String prendaSuperiores, String prendaInferior, String calzados, String marcaSuperiores, String marcaInferiores, String marcaCalzados, String colorSuperior, String colorInferior, String colorCalzado, String tallaSuperior, String tallaInferior, String tallaCalzado, double precioSuperior, double precioInferior, double precioCalzado) {
        this.prendaSuperiores = prendaSuperiores;
        this.prendaInferior = prendaInferior;
        this.calzados = calzados;
        this.marcaSuperiores = marcaSuperiores;
        this.marcaInferiores = marcaInferiores;
        this.marcaCalzados = marcaCalzados;
        this.colorSuperior = colorSuperior;
        this.colorInferior = colorInferior;
        this.colorCalzado = colorCalzado;
        this.tallaSuperior = tallaSuperior;
        this.tallaInferior = tallaInferior;
        this.tallaCalzado = tallaCalzado;
        this.precioSuperior = precioSuperior;
        this.precioInferior = precioInferior;
        this.precioCalzado = precioCalzado;
    }

    public String getPrendaSuperiores() {
        return prendaSuperiores;
    }

    public String getPrendaInferior() {
        return prendaInferior;
    }

    public String getCalzados() {
        return calzados;
    }

    public String getMarcaSuperiores() {
        return marcaSuperiores;
    }

    public String getMarcaInferiores() {
        return marcaInferiores;
    }

    public String getMarcaCalzados() {
        return marcaCalzados;
    }

    public String getColorSuperior() {
        return colorSuperior;
    }

    public String getColorInferior() {
        return colorInferior;
    }

    public String getColorCalzado() {
        return colorCalzado;
    }

    public String getTallaSuperior() {
        return tallaSuperior;
    }

    public String getTallaInferior() {
        return tallaInferior;
    }

    public String getTallaCalzado() {
        return tallaCalzado;
    }

    public double getPrecioSuperior() {
        return precioSuperior;
    }

    public double getPrecioInferior() {
        return precioInferior;
    }

    public double getPrecioCalzado() {
        return precioCalzado;
    }
    
}
